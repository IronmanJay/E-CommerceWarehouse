package com.demo.model;

public class GMVOrder {

    private String quarter;
    private int gmv_count;
    private int gmv_amount;

    public String getQuarter() {
        return quarter;
    }

    public void setQuarter(String quarter) {
        this.quarter = quarter;
    }

    public int getGmv_count() {
        return gmv_count;
    }

    public void setGmv_count(int gmv_count) {
        this.gmv_count = gmv_count;
    }

    public int getGmv_amount() {
        return gmv_amount;
    }

    public void setGmv_amount(int gmv_amount) {
        this.gmv_amount = gmv_amount;
    }
}
